#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		ft_putchar(str[i]);
		i++;
	}
}

int	main(int argc, char **argv)
{
	int	nb;

	nb = 1;
	while (nb < argc)
	{
		ft_putstr(argv[nb]);
		ft_putchar('\n');
		nb++;
	}
}
